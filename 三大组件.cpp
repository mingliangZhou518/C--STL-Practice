/*
* 容器：序列式容器和关联式容器
* 序列式容器：元素在容器的位置是由元素进入容器的时间和位置决定
* 关联式容器：容器有自身的规则，元素在容器中的位置是由容器的规则决定
*/

/*
* 算法：质变算法和非质变算法
* 质变算法:指运算过程中会改变区间的内容(拷贝，替换，删除等等)
* 非质变算法：不会改变内容（查找，计数，遍历等等）
*/

/*
* 迭代器：重点学习随机迭代器和双向访问迭代器
* 双向迭代器：++，--访问上一个元素和下一个元素
* 随机访问迭代器：+2可以跳两个元素访问元素
*/

/*
* 三大组件之间的关系：
* 容器存储数据，并且提供迭代器，算法通过迭代器来操作容器中的元素
*/